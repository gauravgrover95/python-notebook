# int list
my_list = [80, 96, 72, 100, 8]
print("my_list = ", my_list)

# string list
people_list = ["Eric", "John", "Michael", "David"]
print(people_list)

# sout index
print(people_list[0])

# sout slicing
print(people_list[1:3])

# appending
people_list.append("Juli")
print(people_list)

# insert
people_list.insert(2, "INSERT")
people_list.insert(4, "INSERT")
print(people_list)

# remove
people_list.remove("Juli")
print(people_list)

# pop
people_list.pop()
print(people_list)

# sort
people_list.sort()
print(people_list)
