"""
Flow Control: If Else Elif
"""

# If-Else
x = 2
if x > 1:
    print("x is greater than 1")
else:
    print("x is not greater than 1")
print("Outside of if statement")
print()


# If-Else-Elif
hour = 18

if hour < 15:
    print("Good morning!")
elif hour < 20:
    print("Good afternoon!")
else:
    print("Good Night!")
